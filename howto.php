            <h2 class="featurette-heading">Achieving Mental Health-Ayurveda Style <span class="text-muted"></span></h2>
            <p class="lead">Today Ayurveda is becoming
increasingly popular with psychiatric patients as a method to reduce drug dependence.
According to Charak Samhita,<br>

हिताहितम सुखं दु:खमायुस्त्स्य हिताहितम l मानं च यत्रोक्त्मायुर्वेद: स उच्चयते ll<br>
Ayurveda deals with good and bad life (Ayu), as well as happy and unhappy life (Ayu). It
explains what promotes health and what affects health adversely. It also deals with what
affects life expectancy and the quality of life.<br>
 A happy person is one: “Who is devoid of mind
and body imbalances, whose senses are perfectly functioning, whose body is full of ojas
(strong immunity), tejas (shine) and prana (energy), well respected, hardworking, brave,
intellectual, scholar, prosperous and can perform all duties without obstacles”<br>
A person is said to have good health if his/her physical and psychological elements are
balanced. Human being is a constitution of mind, body soul and senses (manas, sharir, atma
and indriya respectively). Senses includes psychological senses (gyanindriya) and physical
senses (karmindriya).<br>

Manas is further a constitution of three operational qualities or gunas which are Satva, Rajas
and Tamas. These three gunas define the character of a person. Satva guna is amalgamation
of self-control, knowledge and power to determine right and wrong in life.<br> The qualities
of Rajas guna are to be in motion, violent, envious, authoritative, desirous and confused. The
characteristics of Tamas guna include being dull, inactive, sleepy, lazy and drowsy. The
imbalance of these three guans is responsible for mental illness or Manovikar.<br>

Also, the sharira or body is comprised of three physco-biological elements which are
Vata(Air), Pitta(Fire) and Kapha (Earth). These elements are essentially negative in nature
and hence called as tridoshas (three faults) but they actually protects the body. When these
tridoshas go awry they may affect mind and cause mental illness.<br>

The treatment of Ayurveda focuses on balancing the three gunas and thridoshas. There are
various ayurvedic medicines and herbs that are recommended by Ayurveda practitioners to
balance them and achieve good mental health. Ayurveda is one of the best<br></p>
