            <h2 class="featurette-heading">Science Behind Yoga <span class="text-muted"></span></h2>
            <p class="lead">There are 8 limbs of Yoga called as Ashtang which are:<br>
1. Yama: Yama includes some ethical rules or moral imperatives such as non-violence,
truthfulness, non-stealing, chastity and non-avarice.<br>
2. Niyams: It includes virtuous habits and observances such as clearness of mind,
contentment, self-discipline, studies of vedas, contemplation of god etc.<br>
3. Asanas: Asana is a posture that one can hold for a period of time, staying relaxed, steady,
comfortable and motionless.<br>
4. Pranayam: Pranayam is the control of the breath, from the Sanskrit prana (breath)<br>
5. Pratyahara: Pratyahara is a combination of two Sanskrit words prati (against or contra)
and ahara (bring near, fetch). Pratyahara is drawing within ones awareness. It is a process
of retracting the sensory experience from external objects.<br>

6. Dharana: Dharana means concentration, introspective focus and one-pointedness of
mind. The root of the word is “dhr” meaning to hold, maintain or keep.<br>

7. Dhyana: Dhyana literally means contemplation, reflection and profound, abstract
meditation. Dhyana is contemplating, reflecting on whatever Dharana has focused on.<br>

8. Samadhi: Samadhi literally means putting together, joining, combining with, union,
harmonious whole, trance. Samadhi is oneness with the subject of meditation<br>.</p>
